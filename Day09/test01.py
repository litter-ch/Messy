# 简单面向对象概念
# 将字符串转化为大写     格式:对象.upper()
str = 'python'
str2 = str.upper()
print(f'{str=},{str2=}')

# 将字符串转化为小写    格式:对象.lower()
s = 'ABCDEFG'
s2 = s.lower()
print(f'{s2=}')

# 首字母大写转化    格式:对象.title()
names = 'jack peter marry'
names2 = names.title()
print(f'{names2=}')

# 大小写转换    格式:对象.swapcase
greeting = 'Good Girl'
greeting2 = greeting.swapcase()
print(f'{greeting2=}')