class1 = {1: '张三', 2: '李四', 3: '王五', 4: '刘六'}
class2 = {5: '西施', 6: '杨玉环', 7: '貂蝉', 8: '王昭君'}

# 字典的合并
# 格式:字典.update(合并的字典)
# 改变原字典 不生成新的字典
class1.update(class2)
print(class1)
print(class2)

class3 = {1: '张三', 2: '李四', 3: '王五', 4: '刘六'}
class4 = {5: '西施', 6: '杨玉环', 7: '貂蝉', 8: '王昭君'}
# 新的合并方式: Python3.5 之后出现的
# 格式:{**字典, **字典, ...}
# 原字典不变 生成新的字典
new_class = {**class3, **class4}
print(new_class)
