# \n  换行符
print('anb')
print('a\nb')
# \  转义字符
print('\\')
print('She say:\'I love you\'') # 没有\ 系统会将引号误会报错
print("She say :\"I love you\"")
# \r  回车字符
# print输出函数，默认自动换行
print('hello')
print('world')
# end=空字符串 拼接在一起不换行
print('hello', end='')
print('world')
# \t  水平制表符
print('\thappy')
print('hello',end='\t')
print('world')