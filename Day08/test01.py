x = 100
print('x=%6d' % x)
# '6'输出6个格数 '100'占三个 默认靠右对齐 剩下三个空格来补齐

y = 10.5
print('y=%6.2f' % y)
# '6'输出6个格数 '.2'保留两位小数点 '10.50'占三个 默认右对齐 剩下一个空格补齐

z = "Python"
print('z=%1s' % z)
# 如果空间格数不足, 则会完整显示

x = 100
print('x=%-6d' % x)
# 负号- 改成左对齐
x = 100
print('x=%+6d' % x)
print('z=%+1s' % z)
# 正号+ 右对齐并且要在数值前面加一个+
