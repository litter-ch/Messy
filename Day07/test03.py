# 二进制
num1 = 0b01111
print(num1, type(num1))

num2 = 15  # bin()内置函数 转成二进制
print(bin(num2), type(num2))

# 八进制
num3 = 0o1234567
print(num3, type(num3))

num4 = 342391  # oct()内置函数 转成八进制
print(oct(num4), type(num4))

# 十六进制
num5 = 0x789abcdef
print(num5, type(num5))

num6 = 32456874  # hex()内置函数 转成十六进制
print(hex(num6), type(num6))
