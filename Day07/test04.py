# abs()内置函数返回绝对值
x = -10
print(f'abs()函数的应用: {abs(x)}')

# pow(a,b)内置函数返回a的b次方
x = 2
y = 5
print(f'pow()函数的应用: {pow(x, y)}')

# round()内置函数
print(f'round(1.5)函数的应用: {round(1.5)}')
print(f'round(1.5)函数的应用: {round(2.5)}')
print(f'round(1.5)函数的应用: {round(2.15, 1)}')
print(f'round(1.5)函数的应用: {round(2.25, 1)}')